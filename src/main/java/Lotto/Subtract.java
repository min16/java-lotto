package Lotto;

public class Subtract extends Operator {
    @Override
    public int operate(int num1, int num2) {
        return num1 - num2;
    }

    @Override
    public int hashCode() {
        return super.hashCode();
    }

    @Override
    public boolean equals(Object obj) {
        return super.equals(obj);
    }

    @Override
    public String toString() {
        return "Subtract{}";
    }
}
