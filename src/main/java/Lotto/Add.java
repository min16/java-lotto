package Lotto;

public class Add extends Operator {
    public int operate(int num1, int num2) {
        return num1 + num2;
    }

    @Override
    public String toString() {
        return "Add{}";
    }

    @Override
    public int hashCode() {
        return super.hashCode();
    }

    @Override
    public boolean equals(Object obj) {
        return super.equals(obj);
    }
}
